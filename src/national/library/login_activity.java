    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        // Here we will do the coding for the Admin panel or for the admin
        String uname = jTextField1.getText();
        String upass = jPasswordField1.getText();

        if (!uname.trim().equals("")) {
            if (!upass.trim().equals("")) {
                if (dec == 1) {
                    if (uname.equals(admin_user) && upass.equals(admin_pass)) {
                        jPanel3.setVisible(false);
                        jLabel11.setVisible(false);

                        Admin a = new Admin();
                        a.setVisible(true);
                        this.dispose();

                    } else {
                        JOptionPane.showMessageDialog(this, "Username or Password is Incorrect");
                    }
                } // Here we will we do the coding for the Librarian login
                else if (dec == 2) {

                    int c = 0;
                    try {
                        String dbpass = "null";
                        String query = "SELECT password FROM librarian WHERE user_name='" + uname + "'";

                        dbconnection db = new dbconnection();
                        db.pstmt = db.con.prepareStatement(query);
                        db.rst = db.pstmt.executeQuery();

                        while (db.rst.next()) {
                            c = 1;
                            dbpass = db.rst.getString(1);
                        }

                        if (upass.equals(dbpass)) {
                            jPanel3.setVisible(false);
                            jLabel11.setVisible(false);

                            Librarian l = new Librarian();
                            l.user_name = uname;
                            l.password = upass;
                            l.setVisible(true);
                            this.dispose();

                        } else if (c == 1) {
                            JOptionPane.showMessageDialog(this, "Wrong Password");
                        }

                        db.con.close();
                        db.stmt.close();
                        db.rst.close();

                    } catch (Exception e) {
                        e.getMessage();
                    } finally {
                        if (c == 0) {
                            JOptionPane.showMessageDialog(this, "Username does not Exits");
                        }
                    }
                } // Here we will we do the coding for the Student login
                else if (dec == 3) {
                    int c = 0;
                    try {
                        String dbpass = null;
                        String dbstatus = null;
                        dbconnection db = new dbconnection();
                        db.stmt = db.con.createStatement();
                        db.rst = db.stmt.executeQuery("SELECT password,status FROM student WHERE id='" + uname + "'");
                        while (db.rst.next()) {
                            c = 1;
                            dbpass = db.rst.getString(1);
                            dbstatus = db.rst.getString(2);
                        }
                        if (upass.equals(dbpass)) {
                            if (dbstatus.equals("Active")) {
                                jPanel3.setVisible(false);
                                jLabel11.setVisible(false);
                              
                                Student_front s = new Student_front();
                                s.stud_id = uname;
                                s.stud_password = upass;
                                s.setVisible(true);

                                this.dispose();

                            } else {
                                JOptionPane.showMessageDialog(this, "                    Login Failed\n      You are currently INACTIVE.\n Contact Librarian to change status");
                            }
                        } else if (c == 0) {
                            JOptionPane.showMessageDialog(this, "Student ID does not Exits");
                        } else if (c == 1) {
                            JOptionPane.showMessageDialog(this, "Wrong Password");
                        }

                        db.con.close();
                        db.stmt.close();
                        db.rst.close();

                    } catch (Exception e) {
                        e.getMessage();
                    } finally {

                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Enter Password");
            }
        } else {
            JOptionPane.showMessageDialog(this, " Enter User Name ");
        }
        //JOptionPane.showMessageDialog(this, "Student ID does not Exits");

    }//GEN-LAST:event_jButton4ActionPerformed

