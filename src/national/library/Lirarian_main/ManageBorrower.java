    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        String stu_id = jTextField40.getText();
        String name = jTextField12.getText();
        String batch = jTextField25.getText();
        String department = jTextField37.getText();
        String accession = jTextField11.getText();
        String isbn = jTextField38.getText();
        try {
            dbconnection db = new dbconnection();
            db.stmt = db.con.createStatement();
            db.rst = db.stmt.executeQuery("SELECT stu_id,TYPE, COUNT(TYPE)  FROM transaction_history WHERE stu_id='" + stu_id + "' GROUP BY TYPE");
            int book_tran[] = new int[2];
            int count = 0;

            while (db.rst.next()) {

                book_tran[count] = db.rst.getInt(3);

                count++;
            }
            // if the book issued previously is more than 6 then do no issue book 
            if (book_tran[0] - book_tran[1] <= 6) {

                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                Calendar c = Calendar.getInstance();
                c.setTime(new Date()); // Now use today date.
                String curdate = sdf.format(c.getTime());

                c.add(Calendar.DATE, 7); // Adding 7 days
                String retdate = sdf.format(c.getTime());

                String borrow_date = curdate;
                String return_date = retdate;

                dbconnection dbm = new dbconnection();
                dbm.stmt = dbm.con.createStatement();
                String Query0 = "SELECT availability from book_collection where accession='" + accession + "'";
                dbm.rst = dbm.stmt.executeQuery(Query0);
                if (dbm.rst.next()) {
                    if (dbm.rst.getString(1).equals("YES")) {
                        String Query1 = "INSERT INTO borrower_list(stu_id,name,batch,department,accession,isbn,borrow_date,return_date)VALUES ('" + stu_id + "','" + name + "','" + batch + "','" + department + "','" + accession + "','" + isbn + "','" + borrow_date + "','" + return_date + "');";
                        dbm.stmt.executeUpdate(Query1);
                        String Query2 = "update book_collection set availability = 'NO' where accession = '" + accession + "'";
                        dbm.stmt.executeUpdate(Query2);
                        String Query3 = " INSERT INTO transaction_history(stu_id,stu_name,accession,isbn,date,type,latefee)VALUES ('" + stu_id + "','" + name + "','" + accession + "','" + isbn + "','" + borrow_date + "','ISSUE','--')";
                        dbm.stmt.executeUpdate(Query3);
                        JOptionPane.showMessageDialog(this, "Book Issued");
                        jTextField40.setText("");
                        jTextField11.setText("");
                        jTextField38.setText("");
                        jTextField12.setText("");
                        jTextField25.setText("");
                        jTextField37.setText("");
                    } else {
                        JOptionPane.showMessageDialog(this, "Book already Issued to someone else");
                    }
                }

                dbm.con.close();
                dbm.stmt.close();
                dbm.rst.close();
                db.con.close();
                db.stmt.close();
                db.rst.close();

            } else {
                JOptionPane.showMessageDialog(null, "maximum Number of books already issued");
            }
        } catch (Exception ex) {
            ex.getMessage();
        }


    }//GEN-LAST:event_jButton14ActionPerformed

