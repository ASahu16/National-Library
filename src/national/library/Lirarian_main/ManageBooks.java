    private void jTextField7KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField7KeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int c = 0;
            String accession = jTextField7.getText();

            try {

                dbconnection db = new dbconnection();
                db.stmt = db.con.createStatement();
                db.rst = db.stmt.executeQuery("SELECT * FROM book_collection WHERE accession='" + accession + "'");
                while (db.rst.next()) {
                    c = 1;

                    accession = db.rst.getString(1);
                    String cl_accession = db.rst.getString(2);
                    String isbn = db.rst.getString(3);
                    String book_title = db.rst.getString(4);
                    String authorname = db.rst.getString(5);
                    String edition = db.rst.getString(6);
                    String bookshelf = db.rst.getString(7);
                    String row = db.rst.getString(8);
                    String column = db.rst.getString(9);
                    String availability = db.rst.getString(10);

                    jTextField4.setText(accession);
                    jTextField5.setText(isbn);
                    jTextField6.setText(cl_accession);
                    jTextField21.setText(book_title);
                    jTextField20.setText(authorname);
                    jTextField19.setText(edition);
                    jTextField24.setText(bookshelf);
                    jTextField23.setText(row);
                    jTextField22.setText(column);

                }

                if (c == 0) {
                    JOptionPane.showMessageDialog(this, "Accession number does not Exists");
                }

                db.con.close();
                db.stmt.close();
                db.rst.close();

            } // TODO add your handling code here:
            catch (Exception ex) {
                ex.getMessage();
            }
        } else if (evt.getKeyCode() == KeyEvent.VK_BACKSPACE) {
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField21.setText("");
            jTextField20.setText("");
            jTextField19.setText("");
            jTextField24.setText("");
            jTextField23.setText("");
            jTextField22.setText("");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jTextField7KeyReleased

