    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        try {
            String c = jTextField7.getText();
            String accession = jTextField4.getText();
            String cl_accession = jTextField6.getText();
            String isbn = jTextField5.getText();
            String book_title = jTextField21.getText();
            String authorname = jTextField20.getText();
            String edition = jTextField19.getText();
            String bookshelf = jTextField24.getText();
            String b_row = jTextField23.getText();
            String b_column = jTextField22.getText();

            if (accession.equals("") || isbn.equals("") || book_title.equals("") || bookshelf.equals("") || b_row.equals("") || b_column.equals("")) {
                JOptionPane.showMessageDialog(this, "Mandatory Fields cannot be blank");
            } else {

                dbconnection db = new dbconnection();
                db.stmt = db.con.createStatement();
                db.rst = db.stmt.executeQuery("SELECT accession from book_collection where accession='" + accession + "'");
                while (db.rst.next()) {
                    if (!db.rst.getString(1).equals(c)) {
                        JOptionPane.showMessageDialog(this, "Accession Number Already exists  \n            Try different.");
                    } else {
                        db.rst = db.stmt.executeQuery("Select accession from book_collection where b_row='" + b_row + "' AND bookshelf='" + bookshelf + "'  AND b_column='" + b_column + "'");
                        while (db.rst.next()) {
                            if (!db.rst.getString(1).equals(c)) {
                                JOptionPane.showMessageDialog(this, "Selected placed in already occupied\n\nChange Bookshelf, row or column.");
                            } else {

                                String Query = "UPDATE book_collection SET accession = '" + accession + "',cl_accession = '" + cl_accession + "',isbn = '" + isbn + "',book_title='" + book_title + "',authorname='" + authorname + "',edition = '" + edition + "',bookshelf='" + bookshelf + "',b_row = '" + b_row + "',b_column = '" + b_column + "'  WHERE accession = '" + c + "'";        // TODO add your handling code here:

                                db.stmt.executeUpdate(Query);
                                JOptionPane.showMessageDialog(this, "Information Updated Successfully");
                                jTextField7.setText("");
                                jTextField4.setText("");
                                jTextField5.setText("");
                                jTextField6.setText("");
                                jTextField19.setText("");
                                jTextField20.setText("");
                                jTextField21.setText("");
                                jTextField22.setText("");
                                jTextField23.setText("");
                                jTextField24.setText("");

                            }
                        }
                    }
                }

                db.con.close();
                db.stmt.close();
                db.rst.close();
                
            }

        } catch (Exception e) {
            e.getMessage();
        }

    }//GEN-LAST:event_jButton11ActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        try {

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();

            // get the selected row index
            int selectedRowIndex = jTable2.getSelectedRow();

            // set the selected row data into jtextfields
            String status = (model.getValueAt(selectedRowIndex, 9).toString()).toUpperCase();

            if (status.equals("ACCEPTED")) {
                jRadioButton1.setSelected(true);
            } else if (status.equals("REJECTED")) {
                jRadioButton2.setSelected(true);
            } else if (status.equals("PENDING")) {
                jRadioButton3.setSelected(true);
            } else {
                buttonGroup1.clearSelection();
            }

        } catch (Exception e) {
            e.getMessage();
        }
    }//GEN-LAST:event_jTable2MouseClicked

