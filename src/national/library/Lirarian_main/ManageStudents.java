    private void jTextField26KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField26KeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int c = 0;
            try {
                String name;
                String id = jTextField26.getText();

                String email;
                String batch;
                String department;
                String contact;
                String status;

                dbconnection dbm = new dbconnection();
                dbm.stmt = dbm.con.createStatement();
                dbm.rst = dbm.stmt.executeQuery("SELECT * FROM student where id='" + id + "'");
                while (dbm.rst.next()) {
                    name = dbm.rst.getString(1);
                    email = dbm.rst.getString(4);
                    batch = dbm.rst.getString(5);
                    department = dbm.rst.getString(6);
                    contact = dbm.rst.getString(7);
                    status = dbm.rst.getString(8);
                    byte[] im = dbm.rst.getBytes(9);
                    try {
                        Image j = new ImageIcon(im).getImage();
                        j = j.getScaledInstance(jLabel6.getWidth(), jLabel6.getHeight(), Image.SCALE_SMOOTH);
                        jLabel6.setIcon(new ImageIcon(j));
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                    jTextField27.setText(name);
                    jTextField34.setText(email);
                    jTextField35.setText(batch);
                    jTextField28.setText(department);
                    jTextField33.setText(contact);
                    jTextField36.setText(id);
                    jRadioButton4.setVisible(true);
                    if (status.equals("Active")) {
                        jRadioButton4.setSelected(true);
                        jRadioButton4.setForeground(Color.green);
                        jRadioButton4.setText("Active");
                    } else {
                        jRadioButton4.setSelected(false);
                        jRadioButton4.setForeground(Color.red);
                        jRadioButton4.setText("Inactive");
                    }

                    c = 1;

                }
                if (c == 0) {
                    JOptionPane.showMessageDialog(this, "Invald Student ID");
                    jTextField27.setText("");
                    jTextField34.setText("");
                    jTextField35.setText("");
                    jTextField28.setText("");
                    jTextField33.setText("");
                    jTextField36.setText("");
                    jRadioButton4.setVisible(false);
                }

                dbm.con.close();
                dbm.stmt.close();
                dbm.rst.close();

            } // TODO add your handling code here:
            catch (Exception ex) {
                ex.getMessage();
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_BACKSPACE) {
            jTextField27.setText("");
            jTextField34.setText("");
            jTextField35.setText("");
            jTextField28.setText("");
            jTextField33.setText("");
            jTextField36.setText("");
            jRadioButton4.setVisible(false);
            jLabel6.setIcon(new javax.swing.ImageIcon(getClass().getResource("/STU_res/icons8_customer_50px.png")));
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jTextField26KeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();

        filename = f.getAbsolutePath();
        ImageIcon imageIcon = new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(img.getWidth(), img.getHeight(), Image.SCALE_DEFAULT));
        img.setIcon(imageIcon);
        try {

            File image = new File(filename);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];

            for (int readNum; (readNum = fis.read(buf)) != -1;) {

                bos.write(buf, 0, readNum);
            }
            stu_image = bos.toByteArray();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }


    }//GEN-LAST:event_jButton1ActionPerformed

