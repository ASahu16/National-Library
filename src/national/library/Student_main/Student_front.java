    private void jLabel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MouseClicked
        try {

            BorrowList bl = new BorrowList();
            bl.stud_id = stud_id;
            bl.stud_password = stud_password;
            String stu_id;
            String name;
            String batch;
            String department;
            String accession;
            String isbn;
            String borrow_date;
            String return_date;
            dbconnection db = new dbconnection();
            db.stmt = db.con.createStatement();
            db.rst = db.stmt.executeQuery("Select * from borrower_list where stu_id='" + stud_id + "'");
            DefaultTableModel t = (DefaultTableModel) bl.jTable1.getModel();
            t.getDataVector().removeAllElements();
            bl.jTable1.setRowHeight(25);
            while (db.rst.next()) {
                stu_id = db.rst.getString(1);
                name = db.rst.getString(2);
                batch = db.rst.getString(3);
                department = db.rst.getString(4);
                accession = db.rst.getString(5);
                isbn = db.rst.getString(6);
                borrow_date = db.rst.getString(7);
                return_date = db.rst.getString(8);

                Object content[] = {stu_id, name, batch, department, accession, isbn, borrow_date, return_date};

                t.addRow(content);
            }

            db.con.close();
            db.stmt.close();
            db.rst.close();

            bl.setVisible(true);
            this.dispose();
        } catch (Exception e) {
            e.getMessage();
        }
    }//GEN-LAST:event_jLabel10MouseClicked

