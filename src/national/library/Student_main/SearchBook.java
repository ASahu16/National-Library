    private void jTextField8KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField8KeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int c = 0;
            try {

                String accession = jTextField8.getText();
                //     String password=jPasswordField1.getText();

                dbconnection db = new dbconnection();
                db.stmt = db.con.createStatement();
                db.rst = db.stmt.executeQuery("SELECT * FROM book_collection  where accession='" + accession + "'");
                while (db.rst.next()) {

                    accession = db.rst.getString(1);
                    String cl_accession = db.rst.getString(2);
                    String isbn = db.rst.getString(3);
                    String book_title = db.rst.getString(4);
                    String authorname = db.rst.getString(5);
                    String edition = db.rst.getString(6);
                    String bookshelf = db.rst.getString(7);
                    String b_row = db.rst.getString(8);
                    String b_column = db.rst.getString(9);
                    String availability = db.rst.getString(10);
                    String borrowed_time = db.rst.getString(11);

                    jTextField1.setText(accession);
                    jTextField2.setText(isbn);
                    jTextField3.setText(cl_accession);
                    jTextField15.setText(book_title);
                    jTextField14.setText(authorname);
                    jTextField13.setText(edition);
                    jTextField18.setText(bookshelf);
                    jTextField17.setText(b_row);
                    jTextField16.setText(b_column);

                    c = 1;

                }
                if (c == 0) {
                    JOptionPane.showMessageDialog(this, "Invald Accession No.");
                }

                db.con.close();
                db.stmt.close();
                db.rst.close();

            } // TODO add your handling code here:
            catch (Exception ex) {
                ex.getMessage();
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) {
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField15.setText("");
            jTextField14.setText("");
            jTextField13.setText("");
            jTextField18.setText("");
            jTextField17.setText("");
            jTextField16.setText("");
        }
    }//GEN-LAST:event_jTextField8KeyReleased

