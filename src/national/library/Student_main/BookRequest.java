    private void jButton24ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton24ActionPerformed
        try {
            String stu_id = this.stud_id;
            String stu_name = null;
            String stu_batch = null;
            String stu_dept = null;
            String book_title;
            String author;
            String edition;
            String status;
            String req_date;
            String last_update;

            dbconnection db = new dbconnection();
            db.stmt = db.con.createStatement();
            db.rst = db.stmt.executeQuery("SELECT name,batch,department FROM student WHERE id='" + stu_id + "'");
            while (db.rst.next()) {
                stu_name = db.rst.getString(1);
                stu_batch = db.rst.getString(2);
                stu_dept = db.rst.getString(3);
            }
            System.out.println(stu_name);
            System.out.println(stu_batch);
            System.out.println(stu_dept);
            book_title = jTextField13.getText();
            author = jTextField14.getText();
            edition = jTextField15.getText();
            status = "Pending";
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            Calendar c = Calendar.getInstance();
            c.setTime(new Date()); // Now use today date.
            req_date = sdf.format(c.getTime());
            last_update = "Not Yet Viewed";
            db.stmt.executeUpdate("INSERT INTO book_request (stu_id,stu_name,stu_batch,stu_dept,book_title,author,edition,status,req_date,last_update)VALUES ('" + stu_id + "','" + stu_name + "','" + stu_batch + "','" + stu_dept + "','" + book_title + "','" + author + "','" + edition + "','" + status + "','" + req_date + "','" + last_update + "')");
            JOptionPane.showMessageDialog(this, "Request Recorded");
            jTextField13.setText("");
            jTextField14.setText("");
            jTextField15.setText("");

             db.con.close();
            db.stmt.close();
            db.rst.close();
            
        } catch (Exception e) {
            e.getMessage();
        }
    }//GEN-LAST:event_jButton24ActionPerformed

